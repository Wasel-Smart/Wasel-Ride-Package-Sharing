import { existsSync, readFileSync } from 'node:fs';
import { join } from 'node:path';

const root = process.cwd();
const checks = [];

function readJson(path) {
  return JSON.parse(readFileSync(join(root, path), 'utf8'));
}

function read(path) {
  return readFileSync(join(root, path), 'utf8');
}

function pass(name, evidence) {
  checks.push({ name, status: 'pass', evidence });
}

function warn(name, evidence) {
  checks.push({ name, status: 'warn', evidence });
}

function fail(name, evidence) {
  checks.push({ name, status: 'fail', evidence });
}

const pkg = readJson('package.json');
const app = readJson('app.json').expo;
const detox = read('.detoxrc.js');

if (pkg.main === 'expo-router/entry') {
  pass('single-ios-entrypoint', 'package main uses expo-router/entry');
} else {
  fail('single-ios-entrypoint', `package main is ${pkg.main}`);
}

for (const file of ['app/_layout.tsx', 'app/index.tsx', 'app/rides.tsx', 'app/packages.tsx', 'app/profile.tsx']) {
  if (existsSync(join(root, file))) pass(`route:${file}`, 'route exists');
  else fail(`route:${file}`, 'route missing');
}

const ios = app.ios ?? {};
if (ios.bundleIdentifier) pass('ios-bundle-id', ios.bundleIdentifier);
else fail('ios-bundle-id', 'missing ios.bundleIdentifier');

const requiredPlist = [
  'NSLocationWhenInUseUsageDescription',
  'NSLocationAlwaysAndWhenInUseUsageDescription',
  'NSCameraUsageDescription',
  'NSFaceIDUsageDescription',
];
for (const key of requiredPlist) {
  if (ios.infoPlist?.[key]) pass(`plist:${key}`, 'configured');
  else fail(`plist:${key}`, 'missing');
}

if (ios.infoPlist?.UIBackgroundModes?.includes('location')) {
  pass('background-location-mode', 'UIBackgroundModes includes location');
} else {
  fail('background-location-mode', 'location background mode missing');
}

if (ios.entitlements?.['aps-environment']) {
  pass('push-entitlement', `aps-environment=${ios.entitlements['aps-environment']}`);
} else {
  fail('push-entitlement', 'missing aps-environment entitlement');
}

const source = [
  'app/_layout.tsx',
  'src/screens/HomeScreen.tsx',
  'src/screens/RideRequestScreen.tsx',
  'src/screens/PackagesScreen.tsx',
  'src/screens/ProfileScreen.tsx',
  'src/components/OfflineBanner.tsx',
].map(read).join('\n');

for (const id of [
  'home-screen',
  'ride-request-screen',
  'packages-screen',
  'profile-screen',
  'home-tab',
  'rides-tab',
  'packages-tab',
  'profile-tab',
  'origin-input',
  'destination-input',
  'seats-input',
  'submit-request-button',
  'package-pickup-input',
  'package-dropoff-input',
  'submit-package-button',
  'online-indicator',
]) {
  if (source.includes(id)) pass(`test-id:${id}`, 'present');
  else fail(`test-id:${id}`, 'missing');
}

if (detox.includes('ios.sim.debug') && detox.includes('Wasel.xcworkspace')) {
  pass('detox-ios-config', 'ios.sim.debug builds Wasel workspace');
} else {
  fail('detox-ios-config', 'missing ios.sim.debug workspace configuration');
}

if (existsSync(join(root, 'eas.json'))) {
  pass('eas-build-config', 'eas.json exists');
} else {
  fail('eas-build-config', 'eas.json missing');
}

if (existsSync(join(root, 'ios', 'Wasel.xcworkspace')) || existsSync(join(root, 'ios', 'Wasel.xcodeproj'))) {
  pass('native-ios-project', 'iOS workspace/project exists');
} else {
  warn('native-ios-project', 'workspace/project will be generated by npm run ios:prebuild on macOS');
}

const failures = checks.filter(check => check.status === 'fail');
const warnings = checks.filter(check => check.status === 'warn');

for (const check of checks) {
  console.log(`${check.status.toUpperCase()} ${check.name}: ${check.evidence}`);
}

console.log(`\nReadiness: ${checks.length - failures.length}/${checks.length} checks passing, ${warnings.length} warning(s).`);

if (failures.length > 0) {
  process.exit(1);
}
