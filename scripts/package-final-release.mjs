#!/usr/bin/env node
import { mkdirSync, existsSync, copyFileSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { execSync } from 'node:child_process';

const outDir = 'release';
const stamp = new Date().toISOString().replace(/[:.]/g, '-');
const bundleDir = join(outDir, `wasel-final-${stamp}`);

mkdirSync(bundleDir, { recursive: true });

const filesToInclude = ['README.md', 'docs/PRODUCTION_DEPLOYMENT_GUIDE.md', '.env.production.example'];
for (const file of filesToInclude) {
  if (existsSync(file)) copyFileSync(file, join(bundleDir, file.replaceAll('/', '_')));
}

if (existsSync('dist')) {
  const target = join(bundleDir, 'dist');
  mkdirSync(target, { recursive: true });
  const copy = (src, dst) => {
    for (const item of readdirSync(src)) {
      const srcPath = join(src, item);
      const dstPath = join(dst, item);
      const st = statSync(srcPath);
      if (st.isDirectory()) {
        mkdirSync(dstPath, { recursive: true });
        copy(srcPath, dstPath);
      } else {
        copyFileSync(srcPath, dstPath);
      }
    }
  };
  copy('dist', target);
}

const manifest = {
  createdAt: new Date().toISOString(),
  bundle: bundleDir,
  includesDist: existsSync('dist'),
  notes: 'Final release bundle generated by package-final-release.mjs',
};
writeFileSync(join(bundleDir, 'release-manifest.json'), JSON.stringify(manifest, null, 2));

try {
  execSync(`tar -czf ${bundleDir}.tar.gz -C ${outDir} ${`wasel-final-${stamp}`}`, { stdio: 'inherit' });
  console.log(`\n✅ Release archive created: ${bundleDir}.tar.gz`);
} catch {
  console.log(`\n⚠️ Could not create tar.gz archive. Folder is ready at: ${bundleDir}`);
}
